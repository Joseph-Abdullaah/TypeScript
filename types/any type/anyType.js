// Any type: allows a variable to hold any type of value.
var randomValue = 10;
console.log(typeof randomValue); // Output: number
console.log(randomValue);
randomValue = "Hello";
console.log(typeof randomValue); // Output: string
console.log(randomValue);
randomValue = true;
console.log(typeof randomValue); // Output: boolean
console.log(randomValue);
randomValue = null;
console.log(typeof randomValue); // Output: object
console.log(randomValue);
randomValue = undefined;
console.log(typeof randomValue); // Output: undefined
console.log(randomValue);
